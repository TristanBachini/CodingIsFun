#!/usr/bin/env python3
"""Using mathematical functions in Python"""
import math

print('sqrt(900) = ', math.sqrt(900))
print('Absolute value of -10 is', math.fabs(-10))
print('Ceiling of 9.2 is', math.ceil(9.2))
print('Ceiling of -9.8 is', math.ceil(-9.8))
print('Floor of 9.2 is', math.floor(9.2))
print('Floor of -9.8 is', math.floor(-9.8))
print('sin of 0 radian is', math.sin(0))
print('cos of 0 radian is', math.cos(0))
print('tan of 0 radian is', math.tan(0))
print('e ^ 1 =', math.exp(1))
print('ln(2.718282) =', math.log(2.718282))
print('log(10) =', math.log10(10))
