#!/usr/bin/env  python3
# Illustrating the string data type

# Ways of declaring strings
string01 = "This is a string in Python"
print(string01)

string02 = 'This is a string in Python'
print(string02)

string03 = '''This is a string in Python'''
print(string03)

string04 = """This is a string in Python"""
print(string04)

# Advantage of ' and "
string05 = "A single quote (') inside a double quote"
print(string05)

string06 = 'Here we have "double quotes" inside single quotes'
print(string06)

# Triple quotes is used for multiline strings
string07 = """Hi! I'm a
multiline
          string"""
print(string07)

string08 = "Hi! I'm a\nmultiline\n          string"
print(string08)

# Starting with Python 3, string are Unicode
string09 = "同学們，你们好吗?"
print(string09)
string10 = "Translation: Classmates, how are you?"
print(string10)
